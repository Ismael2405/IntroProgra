"""Elige un día de la semana en base a un núero del 1-7"""
dia=int(input("Dame un número del 1 al 7:" ))
if dia==1:
    print("pinche lunes")
elif dia==2:
    print("sigue siendo martes??")
elif dia==3:
    print("meh, miercóles")
elif dia==4:
    print("jueves, casi viernes")
elif dia==5:
    print("vamos Francis es viernes en la noche, podemos irnos a embriagar")
elif dia==6:
    print("hoy más, como sea")
elif dia==7:
    print("domingo del señor")
else:
    print("no sabes leer?")