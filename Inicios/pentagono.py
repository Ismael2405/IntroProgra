"""//Hallar el área de un pentágono regular
de 6 cm de lado y con 4cm de apotema"""
print("Ingrese los lados del pentagono (o sea 5, pndj): ")
lado=int(input())
print("Ingrese el tamaño de los lados: ")
tam=int(input())
print("Ingresa la altura del pentagono: ")
alt=int(input())
area=(lado*tam*alt)/2
print("El area es: ",area)